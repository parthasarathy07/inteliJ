package com.java.basic.string;

public class StringMethods {
    public static void main(String[] args) {

        System.out.println("king\"\ngdj");
        String s="king\nqueen";
        System.out.println(s);
        System.out.println("  king \u2002".strip());
        System.out.println("  king \u2002".trim());
    }
}