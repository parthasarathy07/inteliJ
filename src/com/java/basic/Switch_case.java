package com.java.basic;

public class Switch_case
    {
        public static void main(String[]args)
            {
                int val=2;
                switch (val)
                    {
                        case 1 -> System.out.println("monday");
                        case 2 -> System.out.println("tuesday");
                        case 3 -> System.out.println("king");
                    }
            }
    }
